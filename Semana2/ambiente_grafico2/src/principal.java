    void presentar(int pos){
        if(pos>=0 && pos<emp.size() && emp.size()>0){
            this.jTextField1.setText(String.valueOf(emp.get(pos).getId()));
            this.jTextField2.setText(emp.get(pos).getNombre());
        }
    }
