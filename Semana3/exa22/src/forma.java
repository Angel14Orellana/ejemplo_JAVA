    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        model.setRowCount(0);
        for(int i=0;i<emp.size();i++){
            model.addRow(new Object[]{emp.get(i).getNombre(),emp.get(i).getAntiguedad(),emp.get(i).getSueldo_base(), emp.get(i).calculo_deducciones(),emp.get(i).sueldo_a_pagar() });
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed
    void agregar(){
       Empleado temp=new Empleado();
        temp.setNombre(this.jTextField1.getText());
        temp.setAntiguedad(Integer.valueOf(this.jTextField2.getText()));
        temp.setSueldo_base(Double.valueOf(this.jTextField3.getText()));
        
        emp.add(temp);
    }
    void ordenar(){
        Empleado temp=new Empleado();
        for(int x=0;x<emp.size();x++){
            for(int y=1;y<emp.size();y++){
                if(emp.get(y-1).sueldo_a_pagar()<emp.get(y).sueldo_a_pagar()){
                    temp.setNombre(emp.get(y-1).getNombre());
                    temp.setAntiguedad(emp.get(y-1).getAntiguedad());
                    temp.setSueldo_base(emp.get(y-1).getSueldo_base());
                    
                    emp.get(y-1).setNombre(emp.get(y).getNombre());
                    emp.get(y-1).setAntiguedad(emp.get(y).getAntiguedad());
                    emp.get(y-1).setSueldo_base(emp.get(y).getSueldo_base());
                    
                    emp.get(y).setNombre(temp.getNombre());
                    emp.get(y).setAntiguedad(temp.getAntiguedad());
                    emp.get(y).setSueldo_base(temp.getSueldo_base());                                                                                                    
                }
            }
        }
        
        
        
        
    }
