    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        clase_empleado emp=new clase_empleado();
        emp.id=Integer.valueOf(this.jTextField1.getText());
        emp.nombre=this.jTextField2.getText();
        
        arreglo.add(emp);
        limpiar();
        pos=arreglo.size()-1;
        //model.addRow(new Object[]{arreglo.get(pos).getId(),arreglo.get(pos).getNombre()});
        presentar_table();
    }//GEN-LAST:event_jButton1ActionPerformed
void limpiar(){
    this.jTextField1.setText("");
    this.jTextField2.setText("");
}
void presentar_table(){
    model.setRowCount(0);
    for(int i=0;i<arreglo.size();i++){
        model.addRow(new Object[]{arreglo.get(i).getId(),arreglo.get(i).getNombre()});
    }        
}

void presentar(int i){
    
    if(i>=0 && i<arreglo.size()){
        this.jTextField1.setText(String.valueOf(arreglo.get(i).getId()));
        this.jTextField2.setText(arreglo.get(i).getNombre());
    }
    
}
